//establish variable as "0"
int DistanceToIr = 0;

task main()
{
	waitForStart();

	//forward until slightly past IR basket
	while (SensorValue(IRseeker) >= 3)
	{
		motor[LeftMotor] = -40;
		motor[RightMotor] = -40;
	}

	//remembers distance to IR basket, so it can come back the correct distance
	DistanceToIr = nMotorEncoder[LeftMotor];

	//resets motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;


	//turns right, towards the IR basket
	while (nMotorEncoder[LeftMotor] < 950)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = -50;
	}

	//resets motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;

	//flips block into basket
	servo[Flipper] = 50;

	//waits for flipper to complete
	wait1Msec(500);

	//backs up a little bit
	while (nMotorEncoder[LeftMotor] < 100)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = 50;
	}

	//////////WARNING///////////
	//												//
	//	 CODE BEYOND THIS 	  //
	//	 POINT IS NOT					//
	//   GUARENTEED TO WORK		//
	//	 AT ALL								//
	//												//
	////////////////////////////

	//turn back to the left
	while (nMotorEncoder[LeftMotor] > -1300)
	{
		motor[LeftMotor] = -50;
		motor[RightMotor] = 50;
	}

	//resets motors and encoders
	nMotorEncoder[LeftMotor] = 0;
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;

	//makes distance to IR correct distance and direction to leave room for turning
	DistanceToIr = DistanceToIr * -1;
	DistanceToIr = DistanceToIr - 30;

	//goes towards ramp The opposite way it came until distance to IR runs out
	while (nMotorEncoder[LeftMotor] < DistanceToIr)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = 50;
	}

	//turns towards ramp
	while (nMotorEncoder[LeftMotor] > -325)
	{
		motor[LeftMotor] = -50;
		motor[RightMotor] = 50;
	}

	//resets motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;


	//moves forward towards ramp
	while (nMotorEncoder[LeftMotor] < 2880)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = 50;
	}

	//resets motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;

	//turn left towards ramp
	while (nMotorEncoder[LeftMotor] > -600)
	{
		motor[LeftMotor] = -50;
		motor[RightMotor] = 50;
	}

	//reset motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;

	//drive towards ramp
	while (nMotorEncoder[LeftMotor] < 2880)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = 50;
	}

	//reset motors and encoders
	motor[LeftMotor] = 0;
	motor[RightMotor] = 0;
	nMotorEncoder[LeftMotor] = 0;

	while (nMotorEncoder[LeftMotor] > -600)
	{
		motor[LeftMotor] = -50;
		motor[RightMotor] = 50;
	}

	while (nMotorEncoder[LeftMotor] < 1400)
	{
		motor[LeftMotor] = 50;
		motor[RightMotor] = 50;
	}

	//wait for program to complete
	wait1Msec(1000);
}
